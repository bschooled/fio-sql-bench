<#
.SYNOPSIS
Exports a historical fio SQL Bench report across multiple result folders.

.DESCRIPTION
This script scans a results root for per-run `summary.json` artifacts generated by
`Invoke-FioSqlBench.ps1`, rolls them up at the run level, and writes JSON, CSV,
and self-contained HTML output. Use it to compare local and SMB runs over time,
or to review profile baselines across prior benchmark folders.

.PARAMETER ResultsRoot
Root folder that contains prior fio SQL Bench result folders.

.PARAMETER OutputPath
Destination path for the HTML report. Defaults to `historical-report.html` under
the chosen results root.

.PARAMETER JsonPath
Destination path for the aggregated historical JSON model.

.PARAMETER CsvPath
Destination path for the flattened run-level CSV export.

.PARAMETER Profile
Optional profile filter.

.PARAMETER TargetType
Optional target-type filter.

.PARAMETER TargetPathLike
Optional wildcard filter for target paths.

.PARAMETER Newest
Optional limit for the newest N runs after filtering.

.PARAMETER PassThru
Emits the aggregated historical object for automation.

.EXAMPLE
./scripts/Export-FioSqlBenchReport.ps1

.EXAMPLE
./scripts/Export-FioSqlBenchReport.ps1 -Profile Data -TargetType Smb -Newest 10
#>
[CmdletBinding()]
param(
    [string]$ResultsRoot = (Join-Path -Path (Get-Location) -ChildPath 'results'),

    [string]$OutputPath,

    [string]$JsonPath,

    [string]$CsvPath,

    [ValidateSet('Any', 'Data', 'Log', 'Tempdb', 'BackupRestore', 'DbccScan')]
    [string]$Profile = 'Any',

    [ValidateSet('Any', 'Local', 'Smb')]
    [string]$TargetType = 'Any',

    [string]$TargetPathLike,

    [Nullable[int]]$Newest,

    [switch]$PassThru
)

Set-StrictMode -Version Latest
$ErrorActionPreference = 'Stop'

$modulePath = Join-Path -Path $PSScriptRoot -ChildPath '..\src\FioSqlBench\FioSqlBench.psm1'
Import-Module -Name $modulePath -Force

if (-not (Test-Path -LiteralPath $ResultsRoot)) {
    throw "Results root '$ResultsRoot' does not exist."
}

if ([string]::IsNullOrWhiteSpace($OutputPath)) {
    $OutputPath = Join-Path -Path $ResultsRoot -ChildPath 'historical-report.html'
}

if ([string]::IsNullOrWhiteSpace($JsonPath)) {
    $JsonPath = Join-Path -Path $ResultsRoot -ChildPath 'historical-summary.json'
}

if ([string]::IsNullOrWhiteSpace($CsvPath)) {
    $CsvPath = Join-Path -Path $ResultsRoot -ChildPath 'historical-summary.csv'
}

$runs = Import-FioSqlBenchHistory -ResultsRoot $ResultsRoot -Profile $Profile -TargetType $TargetType -TargetPathLike $TargetPathLike -Newest $Newest
if ($runs.Count -eq 0) {
    throw 'No run summaries matched the requested filters.'
}

$rollups = Get-FioHistoricalRollup -Runs $runs
$report = [pscustomobject]@{
    GeneratedUtc = [DateTime]::UtcNow.ToString('o')
    ResultsRoot = $ResultsRoot
    ProfileFilter = $Profile
    TargetTypeFilter = $TargetType
    TargetPathLike = $TargetPathLike
    Newest = $Newest
    RunCount = $runs.Count
    RollupCount = $rollups.Count
    Runs = $runs
    Rollups = $rollups
}

$report | ConvertTo-Json -Depth 12 | Set-Content -Path $JsonPath -Encoding utf8
Export-FioSqlBenchHistoricalCsv -Runs $runs -Path $CsvPath
Export-FioSqlBenchHtmlReport -Runs $runs -Rollups $rollups -Path $OutputPath -Title 'fio SQL Bench Historical Report' -ResultsRoot $ResultsRoot

Write-Host 'Historical report export complete.' -ForegroundColor Green
Write-Host ("  HTML : {0}" -f $OutputPath) -ForegroundColor Gray
Write-Host ("  JSON : {0}" -f $JsonPath) -ForegroundColor Gray
Write-Host ("  CSV  : {0}" -f $CsvPath) -ForegroundColor Gray
Write-Host ("  Runs : {0}" -f $runs.Count) -ForegroundColor Gray

if ($PassThru) {
    $report
}